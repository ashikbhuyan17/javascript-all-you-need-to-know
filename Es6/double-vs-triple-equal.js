var c = '50'
var d = 50
//  triple Equals ( === ) checks for value and data type equality, 
//  Double Equals ( == ) checks for value equality only.
console.log('triple Equals : ', c === d)
console.log('Double Equals : ', c == d);
console.log(c !== d)


