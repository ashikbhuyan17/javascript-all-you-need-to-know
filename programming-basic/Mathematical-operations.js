// Exponentiation operator
var x = 3 ** 4
console.log('Exponentiation = ' + x)

// Increment operator
var x = 3;
// here, x++ means x=x+1 and y=x=x+1 , y is the first assigned by x thn x+1= 3+1 =4
var y = x++;
console.log('x=' + x, '  y=' + y);

var x = 3;
// 
var y = ++x;
console.log('x=' + x, '  y=' + y);


// Decrement operator
var x = 3;
// here, x-- means x=x-1 and y=x=x-1 , y is the first assigned by x thn x-1= 3-1 =2
var y = x--;
console.log('x=' + x, '  y=' + y);

var x = 3;
var y = --x;
console.log('x=' + x, '  y=' + y);

